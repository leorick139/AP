
public class Updates {
	/**
	 * The counter
	 */
	private int CNT = 0;// Starting Value
	public int CNTget() {return CNT;}// Getter for the CNT varaiable
	public void CNTset(int x) {CNT = x;}// Setter for the CNT varaiable
	public void CNTup() {CNT++;}// A +1 incrementer for the CNT varaiable
	
	/**
	 * Constructor
	 */
	public Updates() {}
}
